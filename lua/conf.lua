-- Enable line numbers
vim.opt.number = true
vim.opt.relativenumber = true

-- Ignore case
vim.opt.ignorecase = true

-- Search ignores uppercase: unless the search term contains an uppercase letter
vim.opt.smartcase = true

-- Enable wrapping
vim.opt.wrap = true

-- Preserve indentation of wrapped lines
vim.opt.breakindent = true

-- Tab size
vim.opt.tabstop = 2

-- Make shift width consistent with tab size
vim.opt.shiftwidth = 2

-- hide netrw banner
vim.g.netrw_banner = 0
